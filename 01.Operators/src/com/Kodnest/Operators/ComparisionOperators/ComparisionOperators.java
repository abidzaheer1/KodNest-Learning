package com.Kodnest.Operators.ComparisionOperators;
public class ComparisionOperators {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 10;
		System.out.println("Is a equals to c? "+(a==c));
		System.out.println("Is a not equal to b? "+(a!=b));
		System.out.println("Is a greater than b? "+(a>b));
		System.out.println("Is a less than c? "+(a<c));
		System.out.println("Is a greater than or equal to b? "+(a>=b));
	}
}
