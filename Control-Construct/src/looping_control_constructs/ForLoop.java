package looping_control_constructs;

public class ForLoop {

	public static void main(String[] args) {
		for(int i=200;i<=210;i++) {
			System.out.println("KodNest");
		}
      System.out.println("===================================");
		for(int i=200;i>=191;i--) {
			System.out.println("KodNest");
		}
		
		

	}

}
